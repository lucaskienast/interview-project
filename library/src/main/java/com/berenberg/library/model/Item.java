package com.berenberg.library.model;

import java.util.UUID;

import javax.validation.constraints.NotEmpty;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;
import lombok.ToString;



/*This is a third party library for reducing boilerplates, 
*@Data--- handle the getter and setters
@AllArgsConstructor-- creates a constructor with all parameters
@NoArgsConstructor-- creates a constructor with no argument/parameter in the class
@ToString-- create a toString() method that prints the object as String 
 * **/
@Data
@AllArgsConstructor
@NoArgsConstructor
@ToString
public class Item {

    //Unique Id for each item,it is aautogenerated this is necesary because some items may be multiple
    @NotEmpty(message = "uniqueId cannot be empty")
    private String uniqueId;

    //Assigned id for the idem
    @NotEmpty(message = "ItemId cannot be empty")
    private String itemId;


    // Type of item
    @NonNull
    private String itemType;

    //Full Title of the item
    @NonNull
    private String itemtitle;




    
}
